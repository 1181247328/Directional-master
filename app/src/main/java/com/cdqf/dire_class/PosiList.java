package com.cdqf.dire_class;

public class PosiList {
    private String name;

    private String ble;

    public PosiList(String name, String ble) {
        this.name = name;
        this.ble = ble;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBle() {
        return ble;
    }

    public void setBle(String ble) {
        this.ble = ble;
    }
}
